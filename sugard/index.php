$(document).ready(function(){


  user_data();

  function user_data()
  {
    $.ajax({
      url:"user_data.php",
      method:"POST",
      success:function(data){
        $('#user_data').html(data);
      }
    })
  }

  post_data();

  function post_data()
  {
    $.ajax({
      url:"post_data.php",
      method:"POST",
      success:function(data){
        $('#post_data').html(data);
      }
    })
  }

  $(document).on('click', '.delete', function(){
    var post_id = $(this).attr("id");
    if(confirm("Are you sure you want to delete this?"))
    {
      $.ajax({
        url:"post_delete.php",
        method:"POST",
        data:{post_id:post_id},
        success:function(data)
        {
          post_data();
        }
      });
    }
    else
    {
      return false; 
    }
  });

});
